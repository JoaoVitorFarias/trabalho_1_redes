class OperationService:
    def __init__(self) -> None:
        pass

    def add(self, value1, value2):
        return value1+value2
    
    def sub(self, value1, value2):
        return value1-value2
    
    def mul(self, value1, value2):
        return value1*value2
    
    def div(self, value1, value2):
        return value1/value2